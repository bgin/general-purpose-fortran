write(io,'(a)')'DESCRIPTION'
write(io,'(a)')''
write(io,'(a)')'  Alton''s Homemade Soft Pretzels'
write(io,'(a)')''
write(io,'(a)')'  Alton adapts an old bagel recipe and voila: homemade soft pretzels!'
write(io,'(a)')''
write(io,'(a)')'INGREDIENTS'
write(io,'(a)')''
write(io,'(a)')' o 1 1/2 cups warm (110 to 115 degrees F) water'
write(io,'(a)')' o 1 tablespoon sugar'
write(io,'(a)')' o 2 teaspoons kosher salt'
write(io,'(a)')' o 1 package active dry yeast'
write(io,'(a)')' o 22 ounces all-purpose flour, approximately 4 1/2 cups'
write(io,'(a)')' o 2 ounces unsalted butter, melted'
write(io,'(a)')' o Vegetable oil, for pan'
write(io,'(a)')' o 10 cups water'
write(io,'(a)')' o 2/3 cup baking soda'
write(io,'(a)')' o 1 large egg yolk beaten with 1 tablespoon water'
write(io,'(a)')' o Pretzel salt'
write(io,'(a)')''
write(io,'(a)')'DIRECTIONS'
write(io,'(a)')''
write(io,'(a)')'   Combine the water, sugar and kosher salt in the bowl of a stand mixer'
write(io,'(a)')'   and sprinkle the yeast on top. Allow to sit for 5 minutes or until the'
write(io,'(a)')'   mixture begins to foam. Add the flour and butter and, using the dough'
write(io,'(a)')'   hook attachment, mix on low speed until well combined. Change to medium'
write(io,'(a)')'   speed and knead until the dough is smooth and pulls away from the side'
write(io,'(a)')'   of the bowl, approximately 4 to 5 minutes. Remove the dough from the'
write(io,'(a)')'   bowl, clean the bowl and then oil it well with vegetable oil. Return'
write(io,'(a)')'   the dough to the bowl, cover with plastic wrap and sit in a warm place'
write(io,'(a)')'   for approximately 50 to 55 minutes or until the dough has doubled in size.'
write(io,'(a)')''
write(io,'(a)')'   Preheat the oven to 450 degrees F. Line 2 half-sheet pans with parchment'
write(io,'(a)')'   paper and lightly brush with the vegetable oil. Set aside.'
write(io,'(a)')''
write(io,'(a)')'   Bring the 10 cups of water and the baking soda to a rolling boil in an'
write(io,'(a)')'   8-quart saucepan or roasting pan.'
write(io,'(a)')''
write(io,'(a)')'   In the meantime, turn the dough out onto a slightly oiled work surface and'
write(io,'(a)')'   divide into 8 equal pieces. Roll out each piece of dough into a 24-inch'
write(io,'(a)')'   rope. Make a U-shape with the rope, holding the ends of the rope, cross'
write(io,'(a)')'   them over each other and press onto the bottom of the U in order to form'
write(io,'(a)')'   the shape of a pretzel. Place onto the parchment-lined half sheet pan.'
write(io,'(a)')''
write(io,'(a)')'   Place the pretzels into the boiling water, 1 by 1, for 30 seconds. Remove'
write(io,'(a)')'   them from the water using a large flat spatula. Return to the half sheet'
write(io,'(a)')'   pan, brush the top of each pretzel with the beaten egg yolk and water'
write(io,'(a)')'   mixture and sprinkle with the pretzel salt. Bake until dark golden brown'
write(io,'(a)')'   in color, approximately 12 to 14 minutes. Transfer to a cooling rack'
write(io,'(a)')'   for at least 5 minutes before serving.'
